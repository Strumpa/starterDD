def _build_scheme_descriptor(scheme):
    """Build a directory-name string from a ``DragonCalculationScheme``.

    Format:  ``<SSH_method>_<SSH_spatial>_<N>L_<flux_methods>``

    Examples
    --------
    * 1-level MOC:  ``RSE_IC_1L_MOC``
    * 2-level IC+MOC:  ``RSE_IC_2L_IC_MOC``
    * CP-based:  ``PT_CP_1L_CP``
    """
    ssh_steps = scheme.get_self_shielding_steps()
    flux_steps = scheme.get_flux_steps()

    # Self-shielding descriptor
    if ssh_steps:
        ssh = ssh_steps[0]
        ssh_method = (ssh.self_shielding_method or "RSE").upper()
        ssh_spatial = (ssh.spatial_method or "CP").upper()
    else:
        ssh_method = "UNK"
        ssh_spatial = "UNK"

    # Flux descriptor
    n_levels = len(flux_steps) if flux_steps else 0
    flux_methods = "_".join(
        s.spatial_method.upper() for s in flux_steps
    ) if flux_steps else "NONE"

    # Statepoint count
    n_sp = scheme.get_total_statepoints()
    sp_tag = f"{n_sp}sp" if n_sp > 1 else ""

    parts = [ssh_method, ssh_spatial, f"{n_levels}L", flux_methods]
    if sp_tag:
        parts.append(sp_tag)

    return "_".join(parts)


def _parse_keff_from_listing(listing_path):
    """Parse the last keff value from a Dragon listing file.

    Looks for lines like ``keff= 1.08234`` (printed by the
    generated x2m procedures).

    Returns
    -------
    float or None
    """
    keff = None
    try:
        with open(listing_path) as f:
            for line in f:
                m = re.search(r'keff\s*=\s*([0-9]+\.[0-9]+(?:[eE][+-]?\d+)?)', line)
                if m:
                    keff = float(m.group(1))
    except (OSError, ValueError):
        pass
    return keff


def _check_normal_end(listing_path):
    """Return ``True`` if the listing contains 'normal end'."""
    try:
        with open(listing_path) as f:
            # Check last 30 lines (like rdragon does with tail)
            lines = f.readlines()
            tail = lines[-30:] if len(lines) >= 30 else lines
            return any("normal end" in l.lower() for l in tail)
    except OSError:
        return False


